            <h5>AVIS DE RECRUTEMENT DES AGENTS DE COLLECTE DE LA CARTOGRAPHIE CENSITAIRE PRINCIPALE</h1>
            <p> 
                Dans le cadre de la réalisation du quatrième Recensement Général de la Population et de l’Habitation (RGPH-4), l‘Institut National 
                de la Statistique (INS), à travers le Bureau Central du Recensement (BCR) recherche 750 personnes, en vue de participer à la formation 
                de la cartographie censitaire principale.
            </p>

            <h5>I.	 MISSIONS</h1>
            <p>
                Les travaux de terrain sont importants dans la phase de réalisation de la cartographie censitaire. Le personnel en charge de ces travaux 
                aura pour missions de :
            </p>

            <ul>
                <li>Contacter les autorités administratives et locales et les informer de l’opération de la cartographie censitaire et obtenir leur coopération ;</li>
                <li>Mettre à jour les fonds de cartes (limites administratives, réseau routier, hydrographique, …) ;</li>
                <li>Vérifier et actualiser au besoin le fichier de localité ;</li>
                <li>Administrer le questionnaire communautaire aux autorités et leaders des villages et de quartiers ;</li>
                <li>Procéder à la géolocalisation (prise de coordonnées géographiques) des localités et infrastructures socio-économiques de base ;</li>
                <li>Identifier et géo localiser toutes les concessions dans chaque ilot ;</li>
                <li>Faire le dénombrement des ménages de chaque concession et estimer la population ;</li>
                <li>Proposer un découpage des communes/ sous-préfectures en Zone de Dénombrement (ZD) et Zone de Contrôle (ZC) sur la carte papier ;</li>
                <li>Remplir la fiche de composition des ZD et ZC ;</li>
                <li>Transférer les données collectées au serveur central suivant le protocole de transfert de données qui sera défini ;</li>
                <li>Soumettre un rapport de mission au BCR.</li>
            </ul>
           
            <h5>II.	 PROFIL DU CANDIDAT</h1>
            <ul>
                <li>être de nationalité guinéenne ;</li>
                <li>être âgé de 18 ans au minimum et de 45 ans au maximum, au moment du dépôt du dossier de candidature ; </li>
                <li>Avoir un niveau minimum BAC+3 ;</li>
                <li>Disposer de compétences en géographie, cartographie et géomatique serait un atout ;</li>
                <li>Avoir une expérience en matière de collecte digitale notamment sur tablettes et smartphones est exigée ;</li>
                <li>Savoir conduire une moto un atout ;</li>
                <li>être en bonne santé physique, mentale et intellectuelle ;</li>
                <li>être capable de travailler en équipe et sous pression ;</li>
                <li>avoir des aptitudes à communiquer couramment en français et au moins dans une langue locale ; </li>
                <li>être de bonne moralité ;</li>
                <li>avoir un bon sens des relations humaines ;</li>
                <li>avoir le respect du secret professionnel ;</li>
                <li>avoir un sens de l’organisation et l’esprit d’analyse ;</li>
                <li>être dynamique, rigoureux, disponible et ponctuel ;</li>
                <li>être capable et disponible de travailler en milieu rural sous différentes intempéries ;</li>
                <li>être capable de rédiger un rapport.</li>
            </ul>

            <h5>III. PREALABLES A LA PARTICIPATION A L’ACTIVITE DE COLLECTE</h1>

            <ul>
                <li>a) Le candidat doit être entièrement libre de tout engagement sur la durée de l’activité. La formation va durer vingt et un (21) jours et la collecte s’étendra sur la période de décembre 2023 à juillet 2024.</li>
                <li>b) Ils percevront les frais de transport et un service de restauration sera assuré pendant la formation.</li>
                <li>c) Pendant la phase de collecte, les agents pourront être affectés partout sur le territoire national.</li>
                <li>d) Les dispositions seront prises par le BCR pour assurer le déplacement des agents pendant la phase de collecte.</li>
            </ul>

            <h5>IV.	 DOSSIER DE CANDIDATURE</h1>
            <p>Le dossier de candidature comprendra les documents suivants :</p>

            <ul>
