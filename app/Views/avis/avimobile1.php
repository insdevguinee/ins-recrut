    <h5>AVIS DE RECRUTEMENT DES TECHNICIENS DE LABORATOIRE DE LA CARTOGRAPHIE CENSITAIRE</h5>
    <p> 
        Dans le cadre de la réalisation du quatrième Recensement Général de la Population et de l’Habitation (RGPH-4), l‘Institut National de la Statistique (INS), 
        à travers le Bureau Central du Recensement (BCR) recrute 65 techniciens de laboratoire, en vue de participer à la préparation des fonds de cartes, au traitement 
        des données issues de la collecte du terrain et à l’impression et au colissage des kits cartographiques.
    </p>

    <h5>I.	 MISSIONS</h5>
    <p>
        Placée sous l’autorité de la Direction technique du BCR et sous la supervision technique du chef de l’unité cartographie et de l’Expert International 
        en cartographie censitaire, les techniciens de laboratoire ont pour missions de :
    </p>

    <ul>
        <li>Télécharger les images satellitaires de Très Haute Résolution Spatiale (THRS) ;</li>
        <li>Mosaïquer et compresser les images téléchargées ;</li>
        <li>Faire la mise ensemble des linaires (réseau routier et réseau hydrographique) à actualiser ;</li>
        <li>Catégoriser le réseau routier et hydrographique de toute la Guinée ;</li>
        <li>Centraliser les données et contrôler toutes les erreurs topologiques ;</li>
        <li>Contrôler tous les documents physiques (cartes actualisées) revenus du terrain ;</li>
        <li>Valider la proposition de découpage revenue du terrain ;</li>
        <li>Recevoir et contrôler les données CAPI envoyées au serveur ;</li>
        <li>Actualiser les fonds de cartes de tout le pays (le réseau routier, le réseau hydrographique) ;</li>
        <li>Elaborer les cartes pour le personnel de terrain ;</li>
        <li>Prendre en compte les modifications revenues du terrain pour actualiser les ilots, le réseau routier et le réseau hydrographique ;</li>
        <li>Projeter et vérifier sur SIG la précision et la qualité des données cartographiques collectées sur le terrain ;</li>
        <li>Faire la jointure des tables et découper le territoire en ZD et ZC ;</li>
        <li>Elaborer et exporter les cartes pour le personnel de terrain (cartes de ZD, ZC, de superviseurs et contrôleurs) ;</li>
        <li>Produire des rapports d’étape et un rapport en fin de mission.</li>
    </ul>

    <h3>PROFIL DU CANDIDAT (catégoriser les profil : géomaticiens, agents de validation et d’impression ; Cf. Expert)</h3>
    <ul>
        <li>Être de nationalité guinéenne ;</li>
        <li>Etre âgé de 55 ans au plus à la date de candidature ;</li>
        <li>Avoir un niveau universitaire d’au moins Bac +3 en Géographie, cartographie, géomatique, Système d’Information Géographique, informatique ou tout autre spécialité connexe ;</li>
        <li>Avoir une parfaite maitrise d’un des logiciels cartographiques et de traitement d’images (Qgis, ArcGis ; Illustrator ; Envi ; Global Mapper, …)</li>
        <li>Avoir participé aux travaux de cartographie assistée par ordinateur (CAO) serait un grand atout ;</li>
        <li>être en bonne santé physique, mentale et intellectuelle ;</li>
        <li>être capable de travailler en équipe et sous pression ;</li>
        <li>avoir des aptitudes à communiquer couramment en français et au moins dans une langue locale ; </li>
        <li>être de bonne moralité ;</li>
        <li>avoir un bon sens des relations humaines ;</li>
        <li>avoir le respect du secret professionnel ;</li>
        <li>avoir un sens de l’organisation et l’esprit d’analyse ;</li>
        <li>être dynamique, rigoureux, disponible et ponctuel ;</li>
        <li>être capable et disponible de travailler en milieu rural sous différentes intempéries ;</li>
        <li>être capable de rédiger un rapport.</li>
    </ul>

